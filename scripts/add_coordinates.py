def get_coords(name, address):
    """根据景点名称和地址获取坐标"""
    # 精确匹配
    if name in COORDS:
        return COORDS[name]

    # 模糊匹配
    for key, coords in COORDS.items():
        if key in name or name in key:
            return coords

    # 根据地址匹配城市
    for city, center in CITY_CENTER.items():
        if city in address:
            # 添加小随机偏移避免重叠
            import hashlib
            h = int(hashlib.md5(name.encode()).hexdigest()[:8], 16)
            lat_offset = (h % 100 - 50) * 0.002
            lng_offset = ((h >> 8) % 100 - 50) * 0.002
            return (center[0] + lat_offset, center[1] + lng_offset)

    # 默认贵阳
    return (26.647, 106.630)


def main():
    json_path = os.path.join(os.path.dirname(__file__), 'data', 'guizhou_attractions.json')

    with open(json_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    updated = 0
    for attr in data:
        name = attr['name']
        address = attr.get('address', '')
        lat, lng = get_coords(name, address)
        attr['latitude'] = round(lat, 6)
        attr['longitude'] = round(lng, 6)
        updated += 1

    with open(json_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"已为 {updated} 个景点添加经纬度坐标")

    # 验证
    with open(json_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    has_coords = sum(1 for a in data if a.get('latitude') and a.get('longitude'))
    print(f"验证：{has_coords}/{len(data)} 个景点有坐标")


if __name__ == '__main__':
    main()
